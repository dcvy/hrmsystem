3. Ghi Nhận Thành Quả Và Khen Thưởng Nhân Viên Nhiều Hơn
<br/>

Ghi nhận thành quả trong công việc là một nguồn động lực quan trọng và cần thiết với nhân viên tuy nhiên không phải tổ chức nào cũng làm tốt việc này. Việc đánh giá nhân viên thường niên luôn diễn ra vào cuối năm và đôi khi những thành quả từ đầu năm lại ít được nhớ đến. Sự ghi nhận từ quản lý đến nhân viên nên được thực hiện xuyên suốt trong năm thay vì chỉ tập trung vào kỳ đánh giá giữa năm (mid-year review) và thường niên (year-end appraisal). Để khích lệ nhân viên, các quản lý có thể khen ngợi và tặng thưởng cho nhân viên ngay khi họ hoàn thành một dự án hoặc khi họ làm tốt hơn mong đợi một trọng trách được giao. Phần thưởng có thể đơn giản là phiếu quà tặng, voucher spa …

Một số nhà quản lý có thói quen đặt lời nhắc định kỳ mỗi tháng trên lịch làm việc để nhận xét hiệu suất làm việc của nhóm, đồng thời dành thời gian khen ngợi những cá nhân xuất sắc. Việc tổ chức khen thưởng cho nhân viên được tổ chức dưới nhiều hình thức: trong cuộc họp của nhóm/phòng ban, gửi email khen ngợi, cám ơn trước tập thể công ty…hoặc sử dụng những kênh truyền thông nội bộ như website, intranet hoặc ứng dụng nội bộ. 

Năm 2019, hệ thống BravoHR thống kê có 34,000 tin nhắn ghi nhận, cám ơn và khích lệ từ quản lý đến nhân viên và từ nhân viên dành cho nhau tại 10 công ty, tập đoàn lớn tại Việt Nam. Điều này cho thấy lực lượng lao động trẻ tại Việt Nam ngày càng mong muốn quản lý nhìn nhận những đóng góp và khích lệ kết quả công việc của họ, đồng thời họ cũng muốn bày tỏ sự biết ơn đến sự hợp tác, giúp đỡ của đồng nghiệp khác. 

Hầu hết mọi người đều mong muốn được công nhận, khen ngợi, nhưng nếu các chương trình ghi nhận diễn ra một cách đại trà thì sẽ không còn ý nghĩa. Vậy nên, việc khen thưởng cần bảo đảm đúng người, đúng lúc, đúng việc. Sau đây là một số gợi ý để việc ghi nhận và tưởng thưởng được diễn ra hiệu quả nhất:
<br/>
Liệt kê những hành động tích cực cần được phát huy trong tổ chức. Ghi nhận và khen thưởng cần được dựa trên những hành động tích cực đóng góp cho thành công của tổ chức. Nếu khen ngợi nhân viên quá thường xuyên nhưng không nêu rõ lý do vì sao họ được khen ngợi và tưởng thưởng sẽ làm giảm đáng kể giá trị của sự công nhận, vì bản thân người được khen thưởng không hiểu lý do vì sao họ được thưởng, và những đồng nghiệp khác cũng không học được gì từ việc quan sát sự công nhận này.
Đặt mục tiêu rõ ràng cho nhân viên. Mỗi nhân viên ở bất kỳ vị trí nào cũng cần đạt được sự công nhận, vì vậy họ cần được hiểu rõ yêu cầu của công ty đối với công việc của họ và mục tiêu hướng đến. 
Ghi nhận và khen thưởng phải công khai. Nếu bạn muốn các nhân viên khác học hỏi từ thành công của những cá nhân xuất sắc, hãy khen thưởng công khai, giải thích rõ lý do và nhấn mạnh với nhân viên khác rằng họ cũng có thể đạt được kết quả tương tự. Điều này sẽ giúp nâng cao tinh thần cạnh tranh công bằng lành mạnh, khuyến khích nhân viên tạo ra kết quả công việc tốt nhất và gia tăng lợi nhuận cho doanh nghiệp. 
Khen thưởng, khích lệ nhân viên thường xuyên. Một chương trình khen thưởng thành công nhất định phải đảm bảo tính nhất quán và liên tục trong cách khen và thưởng cho nhân viên. Khen thưởng diễn ra rời rạc không đem đến hiệu quả mà còn có thể gây phản ứng ngược.
Thiết kế chương trình dành riêng cho công ty bạn. Mỗi công ty có văn hoá và môi trường khác biệt. Vậy nên việc thiết kế một chương trình phát triển bền vững, phù hợp với tính cách và nhu cầu của nhân viên bạn là điều hết sức cần thiết và cần được chú trọng. 
          

